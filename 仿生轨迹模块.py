import random

def 生成贝塞尔曲线轨迹(终点X, 终点Y, 步数=4):
    """
    根据给定的终点，生成带有随机弧度的人类拉枪轨迹。
    P0为起点(0,0)，P3为终点(终点X, 终点Y)。
    P1和P2为随机偏移的控制点，用于制造弧度。
    """
    绝对距离 = (终点X**2 + 终点Y**2)**0.5
    if 绝对距离 < 3.0:
        return [(int(终点X), int(终点Y))]

    P0_x, P0_y = 0, 0
    P3_x, P3_y = 终点X, 终点Y

    偏移系数 = random.uniform(0.1, 0.25)
    
    P1_x = 终点X * 0.33 + 终点Y * 偏移系数
    P1_y = 终点Y * 0.33 - 终点X * 偏移系数
    
    P2_x = 终点X * 0.66 + 终点Y * 偏移系数
    P2_y = 终点Y * 0.66 - 终点X * 偏移系数

    绝对轨迹 = []
    for i in range(1, 步数 + 1):
        t = i / 步数
        当前X = (1-t)**3 * P0_x + 3*(1-t)**2 * t * P1_x + 3*(1-t)*t**2 * P2_x + t**3 * P3_x
        当前Y = (1-t)**3 * P0_y + 3*(1-t)**2 * t * P1_y + 3*(1-t)*t**2 * P2_y + t**3 * P3_y
        绝对轨迹.append((当前X, 当前Y))

    相对轨迹 = []
    累计X, 累计Y = 0, 0
    
    for 绝对X, 绝对Y in 绝对轨迹:
        步长X = int(round(绝对X - 累计X))
        步长Y = int(round(绝对Y - 累计Y))
        
        if 步长X != 0 or 步长Y != 0:
            相对轨迹.append((步长X, 步长Y))
            
        累计X += 步长X
        累计Y += 步长Y

    最终误差X = int(终点X) - 累计X
    最终误差Y = int(终点Y) - 累计Y
    if 最终误差X != 0 or 最终误差Y != 0:
         相对轨迹.append((最终误差X, 最终误差Y))

    return 相对轨迹